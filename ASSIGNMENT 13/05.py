marks = int(input("ENTER THE MARKS : "))

if (marks>=75):
    print(f"MARKS : {marks} , GRADE : DISTINCTION")
elif (marks>=60):
    print(f"MARKS : {marks} , GRADE : FIRST CLASS")
elif (marks>=50):
    print(f"MARKS : {marks} , GRADE : SECOND CLASS")
else:
    print(f"MARKS : {marks} , GRADE : FAIL")
    

