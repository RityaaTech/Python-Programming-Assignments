x = int(input("ENTER THE NUMBER : "))
y = int(input("ENTER THE NUMBER : "))

print(" ADDITION ".center(50,"="),end = "\n\n")
print(f"{x} + {y} = {x+y}")

print(" SUBTRACTION ".center(50,"="),end = "\n\n")
print(f"{x} - {y} = {x-y}")

print(" MULTIPLICATION ".center(50,"="),end = "\n\n")
print(f"{x} * {y} = {x*y}")

print(" DIVISION ".center(50,"="),end = "\n\n")
print(f"{x} / {y} = {x/y}")

